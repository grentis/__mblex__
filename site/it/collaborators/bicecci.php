								<p>
									Stefania Bicecci assiste società industriali -anche quotate- e imprese di servizi, nelle loro problematiche di tipo societario e commerciale, anche in sede di contenzioso. Stefania affianca quotidianamente le imprese clienti offrendo la propria consulenza nelle operazioni ordinarie - quali costituzioni di società, acquisto/cessione di azienda, rami d’azienda, accordi di partnership, joint venture, contrattualistica commerciale- straordinarie -fusioni, scissioni, acquisizioni- di management investment plans - operazioni di incentivazione dei key managers - private equity e investimenti al’estero.<br/>
									Stefania accompagna il cliente in ogni fase dell’operazione: dall’attività di due diligence alla fase di predisposizione della documentazione contrattuale necessaria alla negoziazione.<br/>
									Nell’ambito di complesse operazioni societarie e di acquisizione, Stefania, grazie alla sua ventennale esperienza, è in grado di offrire soluzioni “su misura” che ottimizzano gli interessi e gli obiettivi del cliente (modelli di corporate governance, anche a livello di regolamentazione parasociale, modelli di controllo, utilizzo di sistemi partecipativi innovativi).
								</p>
								<p>
									Stefania assiste altresì il cliente, in italiano, in inglese ed in francese nella negoziazione e definizione di ogni tipo di accordo commerciale in Italia e all’estero (quando regolati dalla legge italiana), di fornitura, distribuzione, franchising, agenzia di concessione di licenze.<br/>
									Stefania si occupa di arbitrati, sia nazionali, sia internazionali nelle aree del diritto commerciale e societario
								</p>
								<p>
									A favore dei propri clienti Stefania offre assistenza e consulenza legale nella interpretazione, corretta applicazione e rinegoziazione di contratti bancari e di contratti relativi a strumenti finanziari; nella gestione del contenzioso – anche n fase stragiudiziale – con banche e società finanziarie relativamente a conti correnti, aperture di credito, affidamenti a scadenza e a revoca, mutui e finanziamenti.<br/>
									Particolare cura e attenzione viene dedicata all’assistenza nei rapporti nei quali si evidenziano problematiche specifiche che pregiudicano l’accesso al credito e la “bancabilità” del cliente (scoperti di conto corrente, revoca di affidamenti, segnalazione a “sofferenza “ presso la Centrale dei Rischi in banca d’Italia, protesti di assegni e cambiali con conseguente irrogazione di sanzioni amministrative anche accessorie e pubblicazione nel Registro Informatico dei Protesti presso le CCIAA nonché segnalazione in Centrale d’Allarmi Interbancari in Banca d’Italia.
